def add_numbers(a: int, b: int) -> int:
    """
    Add two integers.
    
    Parameters:
    a (int): The first integer.
    b (int): The second integer.
    
    Returns:
    int: The sum of the two integers.
    """
    return a + b

# Example usage
result = add_numbers(3, 5)
print(result) # Output: 8
